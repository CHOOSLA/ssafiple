def seed_batch2():
    db = SessionLocal()
    Base.metadata.create_all(bind=engine)

    image_urls = {
        loc_id: image_url for loc_id, image_url in db.query(Location.id, Location.image_url).all()
    }

    all_posts = NEW_LOCATION_POSTS + SECOND_VISIT_POSTS
    inserted = 0
    for loc_id, title, content in all_posts:
        post = Post(
            location_id=str(loc_id),
            title=title,
            content=content,
            author=random.choice(AUTHORS),
            password=DEFAULT_PASSWORD,
            image_url=image_urls.get(loc_id),
        )
        db.add(post)
        inserted += 1

    db.commit()
    db.close()
    print(f"Successfully added {inserted} more Gangnam/Yeoksam posts (new locations: {len(NEW_LOCATION_POSTS)}, second visits: {len(SECOND_VISIT_POSTS)}).")


