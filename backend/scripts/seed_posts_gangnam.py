def seed_gangnam_posts(keep_existing: bool):
    db = SessionLocal()
    Base.metadata.create_all(bind=engine)

    if not keep_existing:
        deleted = db.query(Post).delete()
        db.commit()
        print(f"Removed {deleted} existing posts.")

    image_urls = {
        loc_id: image_url for loc_id, image_url in db.query(Location.id, Location.image_url).all()
    }

    inserted = 0
    for loc_id, title, content in POSTS:
        post = Post(
            location_id=str(loc_id),
            title=title,
            content=content,
            author=random.choice(AUTHORS),
            password=DEFAULT_PASSWORD,
            image_url=image_urls.get(loc_id),
        )
        db.add(post)
        inserted += 1

    db.commit()
    db.close()
    print(f"Successfully seeded {inserted} Gangnam/Yeoksam posts into localhub.db!")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="강남역/역삼역 인근 posts 데이터를 채웁니다.")
    parser.add_argument("--keep", action="store_true", help="기존 게시글을 삭제하지 않고 추가만 함")
    args = parser.parse_args()

    seed_gangnam_posts(keep_existing=args.keep)
